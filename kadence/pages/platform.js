import * as React from 'react';
import Head from 'next/head';
import styles from '@/styles/Platform.module.css';
import Image from 'next/image';
import { useRouter } from 'next/router';
import { Dialog } from '@capacitor/dialog';
import { Box, Button, Stack } from '@mui/material/';
import NetworkAPI from '@/lib/networkAPI';

export default function Platform() {
    const router = useRouter();

    async function handleSpotify() {
        const newPlatformData = {
            username: localStorage.getItem('username'),
            musicPlatforms: "Spotify",
        };
        
        try {
            console.log("Try1");
            await NetworkAPI.patch('/api/users/update', newPlatformData).then(router.push("/profile"));
        } catch (err) {
            Dialog.alert({
                title: 'Error',
                message: `An error occurred while submitting your data: ${err.message}.`,
            });
        }
    }

    async function handleAppleMusic() {
        const newPlatformData = {
            username: localStorage.getItem('username'),
            musicPlatforms: "Apple Music",
        };

        try {
            console.log("Try1");
            await NetworkAPI.patch('/api/users/update', newPlatformData).then(router.push("/profile"));
        } catch (err) {
            Dialog.alert({
                title: 'Error',
                message: `An error occurred while submitting your data: ${err.message}.`,
            });
        }
    }

    return (
        <>
            <Head>
                <title>Select Platform</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <meta
                    name="viewport"
                    content="width=device-width, initial-scale=1"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <main className={styles.main}>
                <Box>
                    <Stack spacing={2} alignItems="center">
                        <Image
                            src="/Spotify.jpg"
                            alt="Spotify Logo"
                            width="300"
                            height="150"
                            className={styles.platformImage}
                            priority
                        />
                        <Button variant="contained" onClick={handleSpotify}>
                            Connect to Spotify!
                        </Button>
                        <br />
                        <Image
                            src="/apple-music.jpg"
                            alt="Apple Music Logo"
                            width="300"
                            height="150"
                            className={styles.platformImage}
                            priority
                        />
                        <Button variant="contained" onClick={handleAppleMusic}>
                            Connect to Apple Music!
                        </Button>
                    </Stack>
                </Box>
            </main>
        </>
    );
}